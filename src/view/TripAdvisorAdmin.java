    private void initComboBoxPays() {
        pays = ps.findAll();
        for (Pays pay : pays) {
            jComboBox1.addItem(pay.getNom());
        }
        jComboBox1.addItemListener((e) -> {
            initComboBoxVille();
        });
    }

    private void initComboBoxVille() {
        jComboBox2.removeAllItems();
        jComboBox2.addItem("--SELECT--");
        int index = jComboBox1.getSelectedIndex();
        if (index > 0) {
            Pays p = pays.get(index - 1);
            villes = vs.findVillesByPays(p.getId());
            for (Ville v : villes) {
                jComboBox2.addItem(v.getNom());
            }
        }
    }

    private void initComboBoxTypeHotel() {
        hotelTypes = hts.findAll();
        for (HotelType hotelType : hotelTypes) {
            jComboBox3.addItem(hotelType.getEtoile() + "√©toile");
        }

    }
    
    private void searchAgain(){
        int pay = jComboBox1.getSelectedIndex();
        int vil = jComboBox2.getSelectedIndex();
        int cat = jComboBox3.getSelectedIndex();
        int eta = jComboBox4.getSelectedIndex();
        Pays payss = null;
        Ville ville = null;
        HotelType categ = null;
        Boolean etat = null;
        if (pay > 0) {
            payss = pays.get(pay - 1);
        }
        if (vil > 0) {
            ville = villes.get(vil - 1);
        }
        if (cat > 0) {
            categ = hotelTypes.get(cat - 1);
        }
        if (eta == 1) {
            etat = false;
        } else if (eta == 2) {
            etat = true;
        }
        List<Hotel> res = hs.SearchHotelAdmin(payss, ville, categ, etat);
        hh = new HotelHelper(jTable1, res);
    }

