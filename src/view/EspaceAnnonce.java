private void initComboBoxPays() {
        pays = ps.findAll();
        for (Pays pay : pays) {
            jComboBox5.addItem(pay.getNom());
        }
        jComboBox5.addItemListener((e) -> {
            initComboBoxVille();
        });
    }

    private void initComboBoxVille() {
        jComboBox6.removeAllItems();
        jComboBox6.addItem("--SELECT--");
        int index = jComboBox5.getSelectedIndex();
        if (index > 0) {
            Pays p = pays.get(index - 1);
            villes = vs.findVillesByPays(p.getId());
            for (Ville v : villes) {
                jComboBox6.addItem(v.getNom());
            }
        }
    }

