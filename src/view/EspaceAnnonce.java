    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int num = new Integer(jTextField1.getText());
        int lit = new Integer(jTextField2.getText());
        int prix = new Integer(jTextField6.getText());
        Hotel hotel = ehe.getSelected();
        if (hs.verifierAccepte(hotel)==-1) {
            JOptionPane.showMessageDialog(null, "votre hotel n'est pas encore accepé", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            if (hotel == null ) {
                JOptionPane.showMessageDialog(null, "veuillez selectionner un hotel", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            int res = cs.addChambre(num, lit, prix, hotel);
            if (res == 1) {
                ch = new ChambreHelper(jTable1, cs.getChambresHotel(hotel));
                JOptionPane.showMessageDialog(null, "chambre ajouté avec succes", "INFO", JOptionPane.INFORMATION_MESSAGE);
            }
            if (res == -1) {
                JOptionPane.showMessageDialog(null, "numero chambre existe deja", "ERROR", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

