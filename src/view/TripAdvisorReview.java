    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        User client = (User) Session.getConnectedUser();
        Hotel hotel = rh.getSelected().getChambre().getHotel();
        int note = (int) jSpinner1.getValue();
        if (jTextArea1.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "veuillez commentez", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else if (hotel == null) {
            JOptionPane.showMessageDialog(null, "veuillez selectionner une reservation", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else{
            String comment = jTextArea1.getText();
            revs.createReview(comment, note, client, hotel);
            Reservation r = rh.getSelected();
            r.setCommented(true);
            rs.edit(r);
            
            searchAgain();
            JOptionPane.showMessageDialog(null, "hotel commenté !", "INFO", JOptionPane.INFORMATION_MESSAGE);
            
            
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

