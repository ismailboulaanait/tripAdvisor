    private void searchAgain(){
        int index = jComboBox1.getSelectedIndex();
        Boolean commented = null;
        
        if (index == 1) {
            commented = true;
        }
        if (index == 2) {
            commented = false;
        }
        
        List<Reservation> res = rs.searchReservation((User) Session.getConnectedUser(), commented);
        rh = new ReservationHelper(jTable1, res);
    }
